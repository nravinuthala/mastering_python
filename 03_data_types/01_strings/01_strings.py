string1 = "Hello python. Its time to learn python"

# fetching chars from sting using indexing
print(string1[1])

# slicing string by specifying starting and ending index
print(string1[2:6])
print(string1[6:12])
print(string1[6:])
print(string1[:6])
print(string1[:])

# string size is denoted by a property called length 
print(len(string1))

print(string1[len(string1) - 1])

print(string1[len(string1) - 1])

# strings support negative index to traverse in reverse
print(string1[-1])

print(string1+string1)
print(string1*3)